#      6) დაბეჭდეთ 10 დან 35 მდე ყველა კენტი რიცხვი while loop ის და if else გამოყენებით

num = 10
while num <= 35:
    if num % 2 != 0:  
        print(str(num) + " is odd")
    num += 1

