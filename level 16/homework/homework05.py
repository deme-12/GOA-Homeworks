#      5) დაბეჭდეთ 5 დან 30 მდე ყველა ლუწი რიცხვი while loop ის და if else გამოყენებით

num = 5
while num <= 30:
    if num % 2 == 0:
        print(str(num) + " is even")
    num += 1 

