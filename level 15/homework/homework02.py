# # # ALU (Arithmetic Logic Unit): ახორციელებს ოპერაციებს (მაგ., დამატება, გამოკლება).
# # # Control Unit: მართავს ინსტრუქციების მომწოდებლობას და შესრულებას.
# # # Cache: დროებითი მეხსიერება, რომელიც სჭირდებათ სწრაფად მოწოდებულ მონაცემებს.

