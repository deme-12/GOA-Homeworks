                # CPU

# # # CPU - ცენტრალური პროცესორი
# # # CPU (Central Processing Unit) კომპიუტერის "brain" (გონებაა), რომელიც პასუხისმგებელია მონაცემების დამუშავებასა და ბრძანებების შესრულებაზე.

# # # როგორ მუშაობს CPU:

# # # CPU იღებს ინსტრუქციებს მეხსიერებიდან (RAM) ან დისკიდან.
# # # ამუშავებს ინსტრუქციებს და ხვდება, რა უნდა გააკეთოს.
# # # Execute (სრულდება): CPU ასრულებს ინსტრუქციას (გაფართოებულ მონაცემებს ან კომანდებს).
