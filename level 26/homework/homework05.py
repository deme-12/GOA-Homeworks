# 5) შექმენით ფუნქცია, რომელსაც გადაეცემა წინადადება. თქვენი დავალებაა ამ წინადადების სიტყვები შეაბრუნოთ და დააბრუნოთ(სიტყვების სიმბოლოები არ უნდა იყოს შებრუნებული)  # მაგალითად: "Hello World!" >>> "World! Hello"

def sentece(s):
    lst = s.split()
    return " ".join(lst[::-1])
print(sentece("hello world"))