def number(bus_stops):
    geton = 0
    getoff = 0
    for stop in bus_stops:
        geton += stop[0] #იმ ადამიანების რაოდენობა, რომლებიც ამოვიდნენ მოცემულ გაჩერებაზე.
        getoff += stop[1]
        # იმ ადამიანების რაოდენობას, რომლებიც ჩამოვიდნენ მოცემულ გაჩერებაზე.
    return geton - getoff
    
print(number([[10, 0], [2, 5]]))



